              CALL FIELD_COMPONENT_MESH_COMPONENT_SET(EQUATIONS_SET%DEPENDENT%DEPENDENT_FIELD,FIELD_U_VARIABLE_TYPE, &
                & component_idx,GEOMETRIC_MESH_COMPONENT,ERR,ERROR,*999)
